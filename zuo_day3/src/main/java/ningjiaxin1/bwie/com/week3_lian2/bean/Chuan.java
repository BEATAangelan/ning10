package ningjiaxin1.bwie.com.week3_lian2.bean;

public class Chuan {
    private Object data;
    private String flag;

    public Chuan(Object data, String flag) {
        this.data = data;
        this.flag = flag;
    }

    public Object getData() {
        return data;
    }

    public void setData(Object data) {
        this.data = data;
    }

    public String getFlag() {
        return flag;
    }

    public void setFlag(String flag) {
        this.flag = flag;
    }
}
